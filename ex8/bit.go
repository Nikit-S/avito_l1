package main

import (
	"fmt"
	"strconv"
)

// битовые операции это боль!!!
func main() {
	var l int64
	l = 9223372036854775800

	var val int64
	var pos int64

	fmt.Println(strconv.FormatInt(l, 2))
	fmt.Scan(&val)
	fmt.Scan(&pos)
	if val == 0 {
		fmt.Println(strconv.FormatInt(l&(9223372036854775807-1<<pos), 2))
	} else if val == 1 {
		fmt.Println(strconv.FormatInt(l|1<<pos, 2))
	}

}

